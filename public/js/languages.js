function _translate(key){

    var lang = localStorage.getItem("local");
    if (lang == "" || lang == null) {
        lang = getLanguage();
    } else {
        if ( lang == 'ar') {
            return ar[key];
        } else {
            return en[key];
        }
    }

   return key;

}

function getLanguage() {
    var csrf_token = $("meta[name=csrf_token]").attr("content");
    var params = {csrf_token: encodeURIComponent(csrf_token)};
    var url = site['uri']['public'] + '/language';
     return $.ajax({
        type: "GET",
        url: url,
        data: params,
         async: false

    }).done(function (data) {
       localStorage.setItem('local', data);
         return data;
    })
}
